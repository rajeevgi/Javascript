// 1. Print numbers from 1 to 10.

let result = "";

console.log("Printing numnbers from 1 to 10.");

for(i = 1; i <= 10; i++){
    result += i + "\t";
}

console.log(result);

// 2. Print numbers from 10 to 1.

let result2 = "";

console.log("Printing numnbers from 10 to 1.");

for(i = 10; i >= 1; i--){
    result2 += i + "\t";
}

console.log(result2);